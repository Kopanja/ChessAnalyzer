package com.sbnz.chessanalyzer.model;

public class BoardCounter {

	private int counter;

	public BoardCounter() {
		super();
	}

	public BoardCounter(int counter) {
		super();
		this.counter = counter;
	}

	public int getCounter() {
		return counter;
	}

	public void setCounter(int counter) {
		this.counter = counter;
	}
	
	
	
}
